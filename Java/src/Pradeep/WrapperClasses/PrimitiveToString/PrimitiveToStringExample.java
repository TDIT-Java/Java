package Pradeep.WrapperClasses.PrimitiveToString;

public class PrimitiveToStringExample {
    public static void main(String[] args) {

        // Primitive to String
        int intValue = 100;
        String intString = String.valueOf(intValue);

        char charValue = 'b';
        String charString = String.valueOf(charValue);

        double doubleValue = 10.1;
        String doubleString = String.valueOf(doubleValue);

        float floatValue = 5.5f;
        String floatString = String.valueOf(floatValue);

        boolean boolValue = true;
        String boolString = String.valueOf(boolValue);
    }
}
